package BroCodeCourse;

public class Variables {

	public static void main(String[] args) {
		
		// variables = holds an information
		// primitive = stores data, have 8 types, 1 value, less memory, faster
		// reference = stores reference, unlimited, could have more than 1 value, more memory, slower, begins with a capital letter
		
		// Declaration and Assignment = Initialization
		int x;				// Declaration
		x = 18;				// Assignment
		
		int y = 12;			// Initialization 
		
		float i = 3213.121F;
		long z = 1232132853257340L;	// Long needs to be followed by L
		char symbol = '@';
		boolean j = true;
		String name = "Rasty Espartero";
		
		/*
		 * int = store up to 2 billion
		 * bytes = 1 to 127
		 * long = longer
		 * double = store numbers with fractional value, more precise, do not need F
		 * float = store numbers with fractional value, needs F
		 * */
		
		System.out.println("This letter holds the data: "+name);
		
	}

}
